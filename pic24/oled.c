//write a string, using the cursor as a starting point
void oled_str(char *s) { //does not include null term for screen
    for (int cursor = 0; cursor <= strlen(s)-1; cursor++) {
        if (cursor >= 25) { //is cursor at edge of screen?
            for (int j = 0; j < 3; j++) {
                oled_data(NULL); //just enough whitespace to roll over to next line
            }
        }
        oled_char(s[cursor]); //write next character
    }
}

void oled_setcursor(unsigned char c) {
    charin = c;
}

void oled_clearrow(int row) {
    charin = row << 7; //set index to start of row
    memset(oled_gbuf + charin, 0x00, OLED_WIDTH); //actually clear row
}
