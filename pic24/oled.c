//these operations abstract away from harder internal storage method
inline void oled_setpixel(uint16_t x, uint16_t y) {
    oled_gbuf[(x) + (((y) / 8) * OLED_WIDTH)] |= (1 << ((y) & 7));
}

inline void oled_clearpixel(uint16_t x, uint16_t y) {
    oled_gbuf[(x) + (((y) / 8) * OLED_WIDTH)] &= ~(1 << ((y) & 7));
}

inline void oled_togglepixel(uint16_t x, uint16_t y) {
    oled_gbuf[(x) + (((y) / 8) * OLED_WIDTH)] ^= (1 << ((y) & 7));
}

void oled_clear(void) {
    memset(oled_gbuf, 0x00, sizeof(unsigned char) * (OLED_WIDTH * OLED_HEIGHT / 8)); //clear internal buffer
    oled_flush();
    charin = 0;
}

//bresnham's algorithm, used to draw accurate lines cheaply
//pulled from google
//uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1
void oled_line(oled_pt_t start, oled_pt_t end) {
    int dx = abs(end.x-start.x), sx = start.x < end.x ? 1 : -1;
    int dy = abs(end.y-start.y), sy = start.y < end.y ? 1 : -1; //dy = delta of y0 to y1, sy = sign
    int err = (dx>dy ? dx : -dy)/2, e2; //adjust for inverse lines
 
    for(;;) {
        oled_setpixel(start.x, start.y);
        if (start.x == end.x && start.y == end.y) break; //are we finished?
        e2 = err; //accumulate error over time... pretty cool
        if (e2 >-dx) { err -= dy; start.x += sx; } //which way are we going?
        if (e2 < dy) { err += dx; start.y += sy; }
    }
}

//write a character to global buffer
void oled_char(char c) {
    for (int i = (c * 5); i < ((c+1) * 5); i++) {
        oled_gbuf[charin++] = font[i]; //write the 5*7 character to screen
    }
}

