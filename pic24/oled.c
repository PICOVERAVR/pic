//initialize oled screen, relies on internal Vcc
//mostly copied from adafruit SSD1306 driver github
void oled_init(void) {
    oled_reset();
    oled_command(0xD5); //set display clock div
    oled_command(0x80); //clock ratio of 0x80
    oled_command(0xA8); //set multiplex
    oled_command(OLED_HEIGHT-1); //relies on height
    oled_command(0xD3); //set display offset
    oled_command(0x00); //no offset
    oled_command(0x40); //set start line to line 0
    oled_command(0x8D); //set charge pumps
    oled_command(0x14); //no external Vcc, generate voltage internally
    oled_command(0x20); //set memory mode
    oled_command(0x00); //horizontal addressing mode
    oled_command(0xA1); //??
    oled_command(0xC8); //scan descending
    oled_command(0xDA); //set com pins?
    oled_command(0x12); //??
    oled_command(0x81); //set contrast
    oled_command(0xCF); //related to Vcc for some reason
    oled_command(0xD9); //set precharge detect?
    oled_command(0xF1); //internal Vcc
    oled_command(0xDB); //v com detect
    oled_command(0x40); //?
    oled_command(0xA4); //resume display
    oled_command(0xA6); //normal display
    oled_command(0x2E); //deactivate scroll
    oled_clear();
    oled_displayon();
}
